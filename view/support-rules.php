                Maç Sonucu (5 Set): Karşılaşmanın skoru üzerine oynanan bahis türüdür.
                (3-0,3-1 gibi). Belirli sayıdaki setin tamamlanmaması veya değiştirilmesi
                durumunda bahisler iade edilir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Set Sayısı (5 Set): Karşılaşmanın kaç set üzerinden tamamlanacağı üzerinden
                sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                (3 set ,4set, 5 set)
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Tek/Çift: Setler içinde takımların elde ettikleri sayıların toplamının
                açılan bahisin tek veya çift sayı olacağı üzerine sonuçlandırılır. Bahis
                tek set için ayrı olarak açılmışsa sadece o set için elde edilen değerler
                kabul edilir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Kaç Sette Belirlenen Bitiş Sayısı Aşılır?: Belirlenen bitiş sayısı ile
                anlatılmak istenilen seti kazanan takımın ilk 4 set için 25 sayısının, 5.
                Set için 15 sayısının üzerinde bir sayı ile kazanmasıdır. Maç içinde kaç
                kere bu bitiş sayısı aşılmışsa veya aşılmamışsa bahisler ona göre
                sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Grup Kazananı: Bahisler final grup pozisyonuna göre sonuçlandırılmaktadır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Tur Atlama: Takımlardan herhangi bir tanesi maç öncesinde turnuvadan
                diskalifiye edilirse tüm bahisler iptal edilir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Şampiyonluk: Müsabakanın bir takımın diskalifiye edilmesi veya çekilmesi
                sebebiyle tamamlanamaması durumunda tüm bahisler geçerlidir. Play off
                maçları da dahil olmak üzere bahisler; final lig pozisyonuna göre
                sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Tenis Kuralları
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Genel
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Aşağıda konusu geçen olasılıkların yaşanması durumunda sıralanmış kurallar
                geçerlidir:
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                • Takvim ve/veya müsabaka gününün değişmesi;
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                • Müsabakanın oynanacağı yerin değişmesi;
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                • Müsabakanın kortunun değişmesi durumunda (Kapalı dan açık korta taşınması
                veya tam tersi);
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                • Maç öncesinde veya maç esnasında yüzeyin değişmesi;
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                • Maç tamamlanana kadar, maç tarihinin ertelenmesi veya maçın askıya
                alınması durumunda dahi maç resmi olarak sonuçlanana kadar tüm bahisler
                geçerlidir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Oyunculardan birinin maçtan çekilmesi halinde o ana kadar gerçekleşmiş olan
                bahisler sonuçlandırılır, tamamlanmamış bahisler iade edilir. Buna örnek
                olarak 5 set oynanan bir maçta maç eğer setler 1-1 iken çekilme yaşanırsa
                set skoru üzerine 3-0 ve 0-3 olarak oynanmış bahisler kaybetmiş sayılır.
                Diğer bahisler iade edilir. Maç başlamadan karşılaşmada başka oyuncu
                oynanacağı açıklanırsa tüm bahisler iade edilir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Tie-Break sayıları 1 oyun olarak değer kazanır. Çiftler maçında eğer maç 3.
                Sete uzarsa son tie-break seti 1 oyun olarak değer kazanır. Örnek olarak
                6-0 4-6 10(1)-5(0) biten bir maç 17 oyun sayısı olarak değer kazanır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Maç Kazananı: Maçı hangi oyuncunun kazanacağına dair oynanır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Alt/Üst Toplam Oyun Sayısı: İki rakibin maç içinde kazandıkları oyun
                sayılarının toplamının altında veya üstünde biteceğine dair bahis oynanır
                ve sonuçlandırılır. Tie-break 1 oyun olarak sayılır. Örnek olarak 6-4 6-2
                biten toplam oyun sayısı 16 dır. Sadece o set için açılmışsa o setin oyun
                sayılarının toplamı değerlendirilir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Tek/Çift Toplam Oyun Sayısı: İki rakibin maç içinde kazandıkları oyun
                sayılarının toplamının tek veya çift sayı ile biteceğine dair bahis oynanır
                ve sonuçlandırılır. Tie-break 1 oyun olarak sayılır. Sadece o set için
                açılmışsa o setin oyun sayılarının toplamı değerlendirilir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Maç Sonucu: 3 set veya 5 set üzerinden tam olarak maç skorunun kaç kaç
                biteceğine dair bahis oynanır ve sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Toplam Set Sayısı: 3 set veya 5 set üzerinden maçın tam olarak kaç set
                biteceğine dair bahis oynanır ve sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                X. Seti Kim Kazanır: İlan edilen seti hangi oyuncunun kazanacağına dair
                bahis oynanır ve sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                X. Setin X. Oyununu Kim Kazanacak: İlan edilen setin ve oyunun hangi
                oyuncunun kazanacağına dair bahis oynanır ve sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                N Setin X ve Y oyununu Hangi Oyuncu Kazanır: İlan edilen setteki gene ilan
                edilen oyunları hangi oyuncunun kazanacağına veya berabere kalacağına dair
                bahis oynanır ve sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                X. Set X. Oyun Skoru: Set içinde ilan edilen oyunda bir rakibin diğerine
                hangi skor üstünlüğüne göre sonuçlanacağına dair oynanan bahis türüdür.
                Örneğin: “R. Federer 30 7. Oyun’’ bahsine oynadığınızda R. Federer’in
                rakibine karşı oynadığı 7.oyunda 30 sayısına karşı üstünlükle bitireceğine
                dair oynamışsınızdır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Şampiyonluk: Üzerine bahis yapılan oyuncunun 1. turdan itibaren turnuvaya
                katılmış olması yeterlidir. Bahisler resmi final neticesine göre
                sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Hentbol
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Hentbol Genel
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Tüm bahisler normal süre sonundaki skor üzerinden sonuçlandırılır, aksi
                belirtilmedikçe uzatmalar dahil edilmez. Eleme karşılaşmalarındaki
                uzatmalar veya penaltı atışları dahil değildir. Bir müsabaka açıklanan
                saatte başlamaz ise ve aynı tarihte (yerel zaman) karşılaşmanın
                tamamlanamayacağı açıklanırsa tüm bahisler iade edilir. Ertelenen veya
                askıya alınan maçlar 24 saat içinde tamamlanmaz ise bahisler iade edilir.
                Web sitesinde yanlış başlangıç saati açıklanması bu kural haricinde
                tutulmuştur. Daha önceden kararlaştırılmış bahis marketleri üzerindeki
                bahisler hariç, tüm bahisler iade edilir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                İlk Yarı/İkinci Yarı: Yalnızca ilk yarı veya ikinci yarı sonucuna göre
                bahisler sonuçlandırılır. Müsabaka ilk yarı sonuçlanmadan iptal edilirse,
                tüm bahisler iade edilir. Bir müsabakanın ikinci yarıda iptal edilmesi
                durumunda ilk yarı bahisleri geçerliliğini korur. İkinci yarıdaki skora
                uzatmalar dahil değildir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Toplam (Alt / Üst): Toplam bahisleri iki takımın maç skorundaki toplam
                sayılarının belirlenen sayının altında veya üstünde üzerine bahis alınır ve
                sonuçlandırılır. Aksi belirtilmedikçe uzatma dahil değildir. Devre
                bahislerinde kendi içerisindeki maç skorlarına göre sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Çifte Şans: Maç içinde iki farklı olasılığa aynı anda bahis yapabilmeyi
                sağlar. Örneğin 1x seçeneki (veya takım olarak yazılır Jokerit Helsinki
                veya Beraberlik gibi) için ilk sırada gösterilen ev sahibi takımın
                kazanması veya berabere kalması halinde bahisler kazanmış olarak
                sonuçlandırılır. Uzatmalar dahil değildir. Herhangi bir devre olarak bahis
                açılmışsa sadece o belirlenen periyot içinde geçerlidir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Handikap: Handikap olarak verilen değer iki takımın toplam maç içinde elde
                ettikleri gol sayıları arasındaki farka göre hesaplanır. Uzatmalar
                dahildir. Örnek olarak favori takım için verilen -11.5 değer o takımın en
                az 12 farkla kazanacağına üzerine bahis oynanacağını gösterir. Tam sayı
                olarak değer verilmişse (-11 gibi) eşitlik halinde bahisler iade edilir.
                Belirli bir yarı için bu değer verilmişse o bahis türü o yarı içindeki
                farka göre sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Beraberlikte İade: Taraflardan hangisinin kazanacağı üzerine oynanır. Maçın
                berabere bitmesi halinde bahisler iade edilir. Uzatmalar dahil değildir.
                Herhangi bir devre olarak bahis açılmışsa sadece o belirlenen devre içinde
                geçerlidir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                İlk Yarı / Maç Sonu: İlk yarıyı ve maç sonucunun ikisinin bir arada nasıl
                biteceğine dair sonuçlandırılır. Uzatmalar dahil değildir. Örnek: ‘Ev
                sahibi / Misafir’ seçenekini seçerseniz ilk yarıda ev sahibi takımın önde
                olacağına ve misafir takımın oyunu kazanacağına bahis yapmış olursunuz.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Ev sahibi / Deplasman Toplam: Adı geçen takım tarafından atılacak gollerin
                altında veya üstünde olacağına göre bahis alınır ve sonuçlandırılır.
                Uzatmalar dahil değildir. Herhangi bir devre olarak bahis açılmışsa sadece
                o belirlenen zamandaki takımın goller toplamı geçerlidir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Goller Ev sahibi / Deplasman: Adı geçen takım tarafından atılacak gollerin
                tam sayı olarak bilinmesine göre bahis alınır ve sonuçlandırılır. Uzatmalar
                dahil değildir. Herhangi bir devre olarak bahis açılmışsa sadece o
                belirlenen zamandaki takımın golleri geçerlidir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Tek / Çift: Müsabakada oluşacak maç skorunun toplamının tek ya da çift sayı
                mı biteceği üzerine yapılan bahistir. Aksi belirtilmedikçe uzatma dahil
                değildir. Herhangi bir devre olarak bahis açılmışsa sadece o belirlenen
                zamandaki skor sayı toplamı geçerlidir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                X. Sayıyı İlk Hangi Takım Yapar: Set içinde bahsi açılan sayıya (iki
                takımın toplam sayısına) ilk hangi takımın ulaşacağı üzerine bahis oynanır.
                Uzatmalar dahil değildir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                X Sayısına İlk Hangi Takım Ulaşır: Maç içinde bahsi açılan sayıya ilk hangi
                takımın ulaşacağı üzerine bahis oynanır. Uzatmalar dahil değildir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Kazanma Marjı: İki takımın arasında oluşacak skor farkının ne kadar
                aralıklarla olacağına veya berabere biteceğine dair bahis oynanır,
                sonuçlandırılır. Uzatmalar dahil değildir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                İlk yarı / İkinci Yarı 3 İhtimalli: Devre bahislerini hangi tarafın önde
                bitireceği veya berabere olacağına dair oynanan bahis türüdür. Uzatmalar
                dahil değildir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Hangi Yarı Daha Fazla Gol Olur: İki takımın toplam gol sayısının hangi yarı
                daha fazla veya eşit olacağına göre sonuçlandırılır. Uzatmalar dahil
                değildir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Ev sahibi / Deplasman Toplam: Adı geçen takım tarafından atılacak gollerin
                altında veya üstünde olacağına göre bahis alınır ve sonuçlandırılır.
                Uzatmalar dahil değildir. Herhangi bir devre olarak bahis açılmışsa sadece
                o belirlenen zamandaki takımın goller toplamı geçerlidir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Tur Atlama: Bir takımın turnuvadan adı geçen maç öncesi diskalifiye
                edilmesi durumunda, tüm tur atlama ve kupayı kazanma bahisleri iade edilir.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Grup Kazananı: Final grup pozisyonuna göre bahisler sonuçlandırılır.
            </span></p>
        <p style="font-family: Arial, Verdana; font-size: 10pt; font-style: normal; font-variant-ligatures: normal; font-variant-caps: normal; font-weight: normal;"><span style="color: rgb(255, 255, 255);">
                Şampiyonluk: Play off maçları da dahil olmak üzere bahisler; final lig
